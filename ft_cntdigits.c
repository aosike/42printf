int	ft_cntdigits(int n)
{
	int			i;
	long long	nb;

	nb = n;
	i = 0;
	if (nb < 0)
	{
		i++;
		nb *= -1;
	}
	if (nb == 0)
		i++;
	while (nb != 0)
	{
		i++;
		nb /= 10;
	}
	return (i);
}
